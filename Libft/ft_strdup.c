#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*temp;

	i = 0;
	while (s1[i] != '\0')
		i++;
	temp = (char *)malloc(i + 1);
	if (!temp)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
